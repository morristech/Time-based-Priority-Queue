package priorityqueue;

import java.util.Date;


public class Task{
    String task;
    Date time;
    int priority;

    public Task(String task, Date time, int priority) {
        this.task = task;
        this.time = time;
        this.priority = priority;
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public int getPriority() {
        return priority;
    }

    public void setPriority(int priority) {
        this.priority = priority;
    }
}
